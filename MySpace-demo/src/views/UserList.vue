<template>
  <ContentBase>
    好友列表
    <div class="card" v-for="usr in users" :key="usr.id">
      <div class="card-body">
        <div class="row">
          <div class="col-1">
            <img class="img-fluid" :src="usr.photo" alt="">
          </div>
          <div class="col-11">
            <div class="username">{{ usr.username }}</div>
            <div class="follow">{{ usr.followerCount }}</div>
          </div>
        </div>
      </div>
    </div>
  </ContentBase>
</template>

<script>
import ContentBase from '../components/ContentBase';
import $ from 'jquery';
import { ref } from 'vue';


export default {
  name: 'UserList',
  components: {
    ContentBase,
  },
  setup() {
    let users = ref([]);

    $.ajax({
      url: 'https://app165.acapp.acwing.com.cn/myspace/userlist/',
      type: "get",
      success(resp) {
        users.value = resp;
      }
    })

    return {
      users
    }
  }
}
</script>

